package overloading;

public class OverloadingDemo {
	
	public void sample() {
		
		System.out.println("Inside sample");
		
	}
	
	public void sample(int a) {
		
		System.out.println("Inside sample");
		
	}
	
	public void sample(int a,int b) {
		
		System.out.println("Inside sample");
		
	}
	
	public void sample(double a) {
		
		System.out.println("Inside sample");
		
	}
	
	public void sample(int a,double b) {
		
		System.out.println("Inside sample");
		
	}
	
	public void sample(double a,int b) {
		
		System.out.println("Inside sample");
		
	}
	
	
}
